#include <iostream>
using namespace std;

int main() {
  // Imprimir la tabla del 4
  cout << "Tabla del 4\n";
  for (int i = 1; i <= 10; i++) {
    cout << "4 x " << i << " = " << 4 * i << "\n";
  }
  cout << "\n";

  // Imprimir la tabla del 6
  cout << "Tabla del 6\n";
  for (int i = 1; i <= 10; i++) {
    cout << "6 x " << i << " = " << 6 * i << "\n";
  }
  cout << "\n";

  return 0;
}
